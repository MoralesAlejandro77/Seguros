public class Polizas {
    private long nropoliza;
    private int tieneg;
    private String plan;
    private String ultimop;
    private int codserv;
    private String Servicio;
    private String fechaa;
    private String fechab;
    private String motivob;


    public Polizas() {
    }

    public long getNropoliza() {
        return nropoliza;
    }

    public void setNropoliza(long nropoliza) {
        this.nropoliza = nropoliza;
    }

    public int getTieneg() {
        return tieneg;
    }

    public void setTieneg(int tieneg) {
        this.tieneg = tieneg;
    }

    public String getPlan() {
        return plan;
    }

    public void setPlan(String plan) {
        this.plan = plan;
    }

    public String getUltimop() {
        return ultimop;
    }

    public void setUltimop(String ultimop) {
        this.ultimop = ultimop;
    }

    public int getCodserv() {
        return codserv;
    }

    public void setCodserv(int codserv) {
        this.codserv = codserv;
    }

    public String getServicio() {
        return Servicio;
    }

    public void setServicio(String servicio) {
        Servicio = servicio;
    }

    public String getFechaa() {
        return fechaa;
    }

    public void setFechaa(String fechaa) {
        this.fechaa = fechaa;
    }

    public String getFechab() {
        return fechab;
    }

    public void setFechab(String fechab) {
        this.fechab = fechab;
    }

    public String getMotivob() {
        return motivob;
    }

    public void setMotivob(String motivob) {
        this.motivob = motivob;
    }
}
