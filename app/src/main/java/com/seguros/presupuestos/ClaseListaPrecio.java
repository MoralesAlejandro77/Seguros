package com.seguros.presupuestos;

public class ClaseListaPrecio {
    String tarifa;
    String Plan;
    String edadi;
    String edadf;
    String importe_sepelio;
    String importe_parcela;
    String importe_luto;

    public String getTarifa() {
        return tarifa;
    }

    public void setTarifa(String tarifa) {
        this.tarifa = tarifa;
    }

    public String getPlan() {
        return Plan;
    }

    public void setPlan(String plan) {
        Plan = plan;
    }

    public String getEdadi() {
        return edadi;
    }

    public void setEdadi(String edadi) {
        this.edadi = edadi;
    }

    public String getEdadf() {
        return edadf;
    }

    public void setEdadf(String edadf) {
        this.edadf = edadf;
    }

    public String getImporte_sepelio() {
        return importe_sepelio;
    }

    public void setImporte_sepelio(String importe_sepelio) {
        this.importe_sepelio = importe_sepelio;
    }

    public String getImporte_parcela() {
        return importe_parcela;
    }

    public void setImporte_parcela(String importe_parcela) {
        this.importe_parcela = importe_parcela;
    }

    public String getImporte_luto() {
        return importe_luto;
    }

    public void setImporte_luto(String importe_luto) {
        this.importe_luto = importe_luto;
    }
}
